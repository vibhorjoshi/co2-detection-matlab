function [co2_map, binary_map] = co2_sfa()

clc;
close all;

hdr_file = ...
'D:\downloads\co2-detection-hyperspectral-main\f250923t01p00r13_rfl.hdr';

bin_file = ...
'D:\downloads\co2-detection-hyperspectral-main\f250923t01p00r13_rfl.bin';

threshold = 1.0;

assert(isfile(hdr_file), ...'HDR missing');

assert(isfile(bin_file), ...'BIN missing');

% READ HDR
txt = fileread(hdr_file);

t = regexp( ...
txt,...
'samples\s*=\s*(\d+)',...
'tokens',...
'once');

samples = str2double(t{1});

t = regexp( ...
txt,...
'lines\s*=\s*(\d+)',...
'tokens',...
'once');

lines = str2double(t{1});

t = regexp( ...
txt,...
'bands\s*=\s*(\d+)',...
'tokens',...
'once');

bands = str2double(t{1});

fprintf( ...
'Metadata: %d x %d x %d\n',...
lines,...
samples,...
bands);

% LOAD SMALL SUBSET

cube = multibandread( ...
bin_file,...
[lines samples bands],...
'float32',...
0,...
'bsq',...
'ieee-le',...
{'Row','Range',[1 1000]},...
{'Column','Range',[1 1000]});

cube = double(cube);


% READ WAVELENGTH

w = regexp( ...
txt,...
'wavelength\s*=\s*\{([^}]*)\}',...
'tokens',...
'once');

wavelengths = ...
str2num(w{1});

wavelengths = ...
wavelengths(:);

% DOWNSAMPLE

cube = cube( ...
1:5:end,...
1:5:end,...
:);

[nRows,nCols,~] = ...
size(cube);

% SWIR REGION

swir_mask = ...
wavelengths >=1500 & ...
wavelengths <=2100;

swir_idx = ...
find(swir_mask);

wl_swir = ...
wavelengths(swir_idx);

n_swir = ...
numel(swir_idx);

if n_swir<10

error( ...
'Insufficient SWIR bands');

end

fprintf( ...
'SWIR bands: %d\n',...
n_swir);

% REFERENCE CO2

amp1=0.30;
cen1=1575;
sig1=15;

amp2=0.70;
cen2=2005;
sig2=12;

ref = ...
amp1 .* ...
exp( ...
-0.5*((wl_swir-cen1)/sig1).^2) ...
+ ...
amp2 .* ...
exp( ...
-0.5*((wl_swir-cen2)/sig2).^2);

ref = ...
ref ./ norm(ref);

ref0 = ...
ref - mean(ref);

% SFA

co2_map = ...
zeros(nRows,nCols);

swir_data = ...
cube(:,:,swir_idx);

for r=1:nRows

for c=1:nCols

spec = ...
squeeze( ...
swir_data(r,c,:));

spec = double(spec);

if std(spec)<1e-10

continue;

end

spec = ...
(spec-mean(spec)) ...
/ std(spec);

co2_map(r,c)= ...
dot(spec,ref0) ...
/ n_swir;

end

end

%% =====================================================
% NORMALIZE
%% =====================================================

co2_map( ...
co2_map<0)=0;

mx=max( ...
co2_map(:));

if mx>0

co2_map= ...
co2_map ./ mx;

end

%% =====================================================
% THRESHOLD
%% =====================================================

t_otsu = ...
graythresh( ...
co2_map) ...
* threshold;

binary_map = ...
co2_map > t_otsu;

%% =====================================================
% DISPLAY
%% =====================================================

figure;

imagesc(co2_map);

axis image;

colorbar;

title('SFA CO2');

figure;

imshow(binary_map);

title('Detections');

fprintf( ...
'SFA complete\n');

end
